package org.jatakasource.testcube.model;

import java.io.Serializable;

public abstract class NameDescriptionPojo<ID extends Serializable> implements INameDescription<ID> {
	private static final long serialVersionUID = -4123223288455468748L;

	private String name;
	private String description;

	@Override
	public String getName() {
		return name;
	}

	@Override
	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String getDescription() {
		return description;
	}

	@Override
	public void setDescription(String description) {
		this.description = description;
	}

	@Override
	public String[][] uniqueConstraints() {
		return null;
	}
}
